	 /**批量删除
	 * @param
	 * @throws Exception
	 */
	@RequestMapping(value="/deleteAll")
	@ResponseBody
	public Object deleteAll() throws Exception{
		logBefore(logger, Jurisdiction.getUsername()+"批量删除CheckupPackage");
		if(!Jurisdiction.buttonJurisdiction(menuUrl, "del")){return null;} //校验权限
		PageData pd = new PageData();		
		Map<String,Object> map = new HashMap<String,Object>();
		pd = this.getPageData();
		List<PageData> pdList = new ArrayList<PageData>();
		String DATA_IDS = pd.getString("DATA_IDS");
		if(null != DATA_IDS && !"".equals(DATA_IDS)){
			String ArrayDATA_IDS[] = DATA_IDS.split(",");
			checkuppackageService.deleteAll(ArrayDATA_IDS);
			pd.put("msg", "ok");
		}else{
			pd.put("msg", "no");
		}
		pdList.add(pd);
		map.put("list", pdList);
		return AppUtil.returnObject(pd, map);
	}
	
	 /**导出到excel
	 * @param
	 * @throws Exception
	 */
	@RequestMapping(value="/excel")
	public ModelAndView exportExcel() throws Exception{
		logBefore(logger, Jurisdiction.getUsername()+"导出CheckupPackage到excel");
		if(!Jurisdiction.buttonJurisdiction(menuUrl, "cha")){return null;}
		ModelAndView mv = new ModelAndView();
		PageData pd = new PageData();
		pd = this.getPageData();
		Map<String,Object> dataMap = new HashMap<String,Object>();
		List<String> titles = new ArrayList<String>();
		titles.add("生成该记录的标记");	//1
		titles.add("该记录生成时间");	//2
		titles.add("开始生效时间");	//3
		titles.add("失效时间");	//4
		titles.add("状态");	//5
		titles.add("版本");	//6
		titles.add("系统标记");	//7
		dataMap.put("titles", titles);
		List<PageData> varOList = checkuppackageService.listAll(pd);
		List<PageData> varList = new ArrayList<PageData>();
		for(int i=0;i<varOList.size();i++){
			PageData vpd = new PageData();
			vpd.put("var1", varOList.get(i).getString("WORKER"));	//1
			vpd.put("var2", varOList.get(i).getString("GENERATEDTIME"));	//2
			vpd.put("var3", varOList.get(i).getString("EFFECTIVEFROM"));	//3
			vpd.put("var4", varOList.get(i).getString("EXPIREON"));	//4
			vpd.put("var5", varOList.get(i).getString("STATUS"));	//5
			vpd.put("var6", varOList.get(i).getString("REVISION"));	//6
			vpd.put("var7", varOList.get(i).getString("SYSFLAG"));	//7
			varList.add(vpd);
		}
		dataMap.put("varList", varList);
		ObjectExcelView erv = new ObjectExcelView();
		mv = new ModelAndView(erv,dataMap);
		return mv;
	}
	
